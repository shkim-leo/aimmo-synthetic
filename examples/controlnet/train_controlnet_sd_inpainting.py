# generate random masks
def random_mask(im_shape, ratio=1, mask_full_image=False):
    mask = Image.new("L", im_shape, 0)
    draw = ImageDraw.Draw(mask)
    size = (random.randint(0, int(im_shape[0] * ratio)), random.randint(0, int(im_shape[1] * ratio)))
    # use this to always mask the whole image
    if mask_full_image:
        size = (int(im_shape[0] * ratio), int(im_shape[1] * ratio))
    limits = (im_shape[0] - size[0] // 2, im_shape[1] - size[1] // 2)
    center = (random.randint(size[0] // 2, limits[0]), random.randint(size[1] // 2, limits[1]))
    draw_type = random.randint(0, 1)
    if draw_type == 0 or mask_full_image:
        draw.rectangle(
            (center[0] - size[0] // 2, center[1] - size[1] // 2, center[0] + size[0] // 2, center[1] + size[1] // 2),
            fill=255,
        )
    else:
        draw.ellipse(
            (center[0] - size[0] // 2, center[1] - size[1] // 2, center[0] + size[0] // 2, center[1] + size[1] // 2),
            fill=255,
        )

    return mask


def log_validation(
    vae, text_encoder, tokenizer, unet, controlnet, args, accelerator, weight_dtype, global_step, epoch, val_cnt=8
):
    logger.info("Running validation... ")
    controlnet = accelerator.unwrap_model(controlnet)
    feature_extractor = CLIPImageProcessor.from_pretrained(
        os.path.join(args.pretrained_model_name_or_path, "feature_extractor")
    )
    # image_encoder = CLIPVisionModelWithProjection.from_pretrained(
    #     os.path.join(args.pretrained_model_name_or_path, "image_encoder")
    # )

    pipeline = StableDiffusionControlNetInpaintPipeline.from_pretrained(
        args.pretrained_model_name_or_path,
        vae=vae,
        text_encoder=text_encoder,
        tokenizer=tokenizer,
        unet=unet,
        controlnet=controlnet,
        feature_extractor=feature_extractor,
        # image_encoder=image_encoder,
        safety_checker=None,
        revision=args.revision,
        variant=args.variant,
        torch_dtype=weight_dtype,
    )
    pipeline = pipeline.to(accelerator.device)
    pipeline.set_progress_bar_config(disable=True)

    if args.enable_xformers_memory_efficient_attention:
        pipeline.enable_xformers_memory_efficient_attention()

    if args.seed is None:
        generator = None
    else:
        generator = torch.Generator(device=accelerator.device).manual_seed(args.seed)

    images = []

    annotations = []
    with open(os.path.join(args.train_data_dir, "train.jsonl"), "r", encoding="utf-8") as f:
        for line in f:
            annotations.append(json.loads(line))

    for idx, anno in enumerate(annotations[:val_cnt]):
        with open(os.path.join(args.train_data_dir, anno["annotation"]), "r") as f:
            annotation = json.load(f)

        image_path = os.path.join(args.train_data_dir, anno["image"])
        control_path = os.path.join(args.train_data_dir, anno["conditioning_images"])
        caption = anno["text"]

        val_image = Image.open(image_path).convert("RGB")
        val_control_image = Image.open(control_path)

        generation_mask_num = random.randint(1, len(annotation["annotations"]))
        generation_mask_num = 3 if generation_mask_num >= 3 else generation_mask_num
        target_anno = random.sample(annotation["annotations"], generation_mask_num)  # generation_mask_num

        mask = np.zeros((val_image.height, val_image.width))

        for ta in target_anno:
            points = np.array(ta["points"], dtype=np.int32)

            try:
                mask = cv2.fillPoly(mask, [points], color=255)
            except:
                continue

        if not mask.any():
            raise Exception("mask sum is zero")

        mask = Image.fromarray(mask)

        with torch.autocast("cuda"):
            image = pipeline(
                prompt=caption,
                image=val_image,
                control_image=val_control_image,
                mask_image=mask,
                height=args.resolution,
                width=args.resolution,
                generator=generator,
            ).images[0]
        images.append(image)

        mask = mask.convert("RGB")

        if epoch == 0:
            val_image.save(os.path.join(args.output_dir, "validation", "{}".format(annotation["filename"])))
            val_control_image.save(
                os.path.join(args.output_dir, "validation", "condition_{}.png".format(annotation["filename"]))
            )

        mask.save(os.path.join(args.output_dir, "validation", "{}_{}_mask.png".format(annotation["filename"], epoch)))
        image.save(os.path.join(args.output_dir, "validation", "{}_{}.png".format(annotation["filename"], epoch)))

    for tracker in accelerator.trackers:
        if tracker.name == "tensorboard":
            np_images = np.stack([np.asarray(img) for img in images])
            tracker.writer.add_images("validation", np_images, global_step, dataformats="NHWC")
        elif tracker.name == "wandb":
            tracker.log(
                {
                    "validation": [
                        wandb.Image(image, caption=f"{i}: {args.validation_prompts[i]}")
                        for i, image in enumerate(images)
                    ]
                }
            )
        else:
            logger.warn(f"image logging not implemented for {tracker.name}")

    del pipeline
    torch.cuda.empty_cache()

    return images


